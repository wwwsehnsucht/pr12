const express = require("express");
const fs = require("fs").promises;
const path = require("path");

const app = express();
const PORT = 3000;
const DATA_FILE = path.join(__dirname, "data.json");

app.use(express.json());

const readTasksFromFile = async () => {
  try {
    const data = await fs.readFile(DATA_FILE, "utf8");
    return JSON.parse(data);
  } catch (error) {
    if (error.code === "ENOENT") return [];
    throw error;
  }
};

const writeTasksToFile = async (tasks) => {
  await fs.writeFile(DATA_FILE, JSON.stringify(tasks, null, 2));
};

app.get("/tasks", async (req, res) => {
  try {
    const tasks = await readTasksFromFile();
    res.json({ message: "tasks:", tasks });
  } catch (error) {
    console.error("error while reading file:", error);
    res.status(500).json({ error: "server error while reading file" });
  }
});

app.get("/tasks/:id", async (req, res) => {
  try {
    const id = parseInt(req.params.id);
    const tasks = await readTasksFromFile();
    const task = tasks.find((t) => t.id === id);

    if (!task) return res.status(404).json({ error: "task not found" });

    res.json({ message: "task found", task });
  } catch (error) {
    console.error("error while fetching task:", error);
    res.status(500).json({ error: "server error" });
  }
});

app.post("/tasks", async (req, res) => {
  try {
    const { text } = req.body;
    if (!text)
      return res.status(400).json({ error: "task description required" });

    const tasks = await readTasksFromFile();
    const newTask = {
      id: tasks.length > 0 ? Math.max(...tasks.map((t) => t.id)) + 1 : 1,
      text,
      completed: false,
    };

    tasks.push(newTask);
    await writeTasksToFile(tasks);

    res.status(201).json({ message: "task added", task: newTask });
  } catch (error) {
    console.error("error while adding task:", error);
    res.status(500).json({ error: "server error while adding task" });
  }
});

app.put("/tasks/:id", async (req, res) => {
  try {
    const id = parseInt(req.params.id);
    const { text, completed } = req.body;

    const tasks = await readTasksFromFile();
    const task = tasks.find((t) => t.id === id);

    if (!task) return res.status(404).json({ error: "task not found" });

    if (text !== undefined) task.text = text;
    if (completed !== undefined) task.completed = completed;

    await writeTasksToFile(tasks);

    res.json({ message: "task updated", task });
  } catch (error) {
    console.error("error while updating task:", error);
    res.status(500).json({ error: "server error while updating task" });
  }
});

app.delete("/tasks/:id", async (req, res) => {
  try {
    const id = parseInt(req.params.id);
    const tasks = await readTasksFromFile();
    const index = tasks.findIndex((t) => t.id === id);

    if (index === -1) return res.status(404).json({ error: "task not found" });

    const deletedTask = tasks.splice(index, 1)[0];
    await writeTasksToFile(tasks);

    res.json({ message: "task deleted", task: deletedTask });
  } catch (error) {
    console.error("error while deleting task", error);
    res.status(500).json({ error: "server error while deleting task" });
  }
});

app.delete("/tasks", async (req, res) => {
  try {
    await writeTasksToFile([]);
    res.json({ message: "every tasks is deleted" });
  } catch (error) {
    console.error("error while cleaning the tasks", error);
    res.status(500).json({ error: "server error while cleaning the tasks" });
  }
});

app.listen(PORT, () => {
  console.log(`http://localhost:${PORT}`);
});
